#include <stdio.h>

int main() {
    int score = 90.5;  
    printf("score : %.d\n", score);
    return 0;
}
// ผลลัพธ์ที่ได้ต่างกันอย่างไรต่างกันตรงที่ถ้าใช้ int จะทำให้ผลลัพธ์เท่ากับ 90 ถ้าใช้ float จะทำให้ผลลัพธ์เท่ากับ 90.5

// ทำไมต้องเลือกชนิดข้อมูลให้ถูกต้อง เพราะ ถ้าเลือกชนิดข้อมูลไม่ถูกจะทำให้ผลลัพธ์ที่แสดงออกมาตรงตามที่กำหนด

// สรุปผลการทดลอง คือ การทดลองนี้ทำให้เห็นการใช้งานของชนิดข้อมูลที่แตกต่างกันและจะทำให้ได้ผลลัพธ์ที่ต่างกันออกไป